public class Condicionais {

  public static void main(String[] args) {

    int time = 22;
    if (time < 10) {
      System.out.println("Good Morning");
    }
    if (time < 18) {
      System.out.println("Good Day");
    } else {
      System.out.println("Good Everning");
    }
  }
}